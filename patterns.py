def printPattern01(n):
    for i in range(n):                      #for upper half

        for j in range(2*(n-i-1)):          #to print '-' in starting
            print(end='-')

        for k in range(4*i+1):

            if(k%2==0 and k<=(4*i+1)/2):    # to print alphabets in decreasing order
                char1 = n - (int(k/2)) + 96     #char1 stores the unicode value to get corresponding character
                print((chr(char1)),end='')

            elif(k%2==0 and k>(4*i+1)/2):   #to print alphabets in increasing order
                char1+=1                    #char1 is same and is being incremented to get the next alphabet
                print(chr(char1),end='')

            else:                           # to print '-'
                print('-',end='')

        for l in range(2*(n-i-1)):          #to print '-' in ending
            print(end='-')

        print()                             # to just move to the next line

    #################################################################################################
    for i in range(n-2,-1,-1):              #for lower half

        for j in range(2*(n-i-1)):          #to print '-' in starting
            print(end='-')

        for k in range(4*i+1):

            if(k%2==0 and k<=(4*i+1)/2):    # to print alphabets in decreasing order
                char1 = n - (int(k/2)) + 96     #char1 stores the unicode value to get corresponding character
                print((chr(char1)),end='')

            elif(k%2==0 and k>(4*i+1)/2):   #to print alphabets in increasing order
                char1+=1                    #char1 is same and is being incremented to get the next alphabet
                print(chr(char1),end='')

            else:                           # to print '-'
                print('-',end='')

        for l in range(2*(n-i-1)):          #to print '-' in ending
            print(end='-')

        print()                             # to just move to the next line


