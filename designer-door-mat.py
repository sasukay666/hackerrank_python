#BLL
def pattern(n, m):
    '''This function prints a pattern. Enter n and m, where m is 3 times n and n is odd'''

    c = '.|.'

    for i in range(1, n, 2):
        s = c*i
        print(s.center(m, "-"))

    print('WELCOME'.center(m, '-'))

    for i in range(n-2, -1, -2):
        s = c*i
        print(s.center(m, "-"))

#PL
if __name__ == "__main__":
    n, m = map(int, input().split())
    pattern(n, m)
